# ────────────────────────────────────────
# 통합 인터페이스
# ────────────────────────────────────────

class SummaryMode:
    QUICK    = "quick"
    NORMAL   = "normal"
    DETAILED = "detailed"

SUMMARY_MODE_LABELS = {
    SummaryMode.QUICK:    "⚡ 빠른 요약 (시험 대비)",
    SummaryMode.NORMAL:   "📝 일반 요약 (평소 복습)",
    SummaryMode.DETAILED: "📖 상세 요약 (강의 대체)",
}

_PROMPT_BUILDERS = {
    SummaryMode.QUICK:    build_quick_prompt,
    SummaryMode.NORMAL:   build_normal_prompt,
    SummaryMode.DETAILED: build_detailed_prompt,
}


def build_prompt(
    mode: str = SummaryMode.NORMAL,
    subject_category: str = "자동 감지",
    subject_custom: str = "",
) -> str:
    """
    최종 프롬프트를 반환하는 통합 함수.

    Args:
        mode:             SummaryMode 상수 (quick / normal / detailed)
        subject_category: 드롭다운 선택값 (SUBJECT_CATEGORIES의 키)
        subject_custom:   사용자 직접 입력값 (직접입력이 드롭다운보다 우선)

    Returns:
        완성된 프롬프트 문자열
    """
    builder = _PROMPT_BUILDERS.get(mode, build_normal_prompt)
    return builder(subject_category=subject_category, subject_custom=subject_custom)


# ────────────────────────────────────────
# 하위 호환성: 기존 DEFAULT_PROMPT 유지
# ────────────────────────────────────────

DEFAULT_PROMPT = build_prompt(
    mode=SummaryMode.NORMAL,
    subject_category="자동 감지",
)
