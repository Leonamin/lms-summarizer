# ────────────────────────────────────────
# 공통 헬퍼: 분야 컨텍스트 문자열 생성
# ────────────────────────────────────────

def _build_subject_context(subject_category: str = "", subject_custom: str = "") -> str:
    """
    드롭다운(subject_category)과 직접입력(subject_custom)을 조합하여
    프롬프트에 삽입할 분야 컨텍스트 문자열을 반환합니다.

    우선순위: 직접입력 > 드롭다운 > 자동 감지
    """
    custom = subject_custom.strip()
    category_desc = SUBJECT_CATEGORIES.get(subject_category, "")

    if custom:
        # 직접 입력이 있으면 최우선
        return f"이 강의는 [{custom}] 분야입니다. 해당 분야의 전문 용어와 개념을 정확히 반영해주세요."
    elif category_desc:
        # 드롭다운 선택
        return f"이 강의는 [{category_desc}] 분야입니다. 해당 분야의 전문 용어와 개념을 정확히 반영해주세요."
    else:
        # 자동 감지
        return (
            "강의 내용을 분석하여 어떤 분야(전공/교양)인지 먼저 판단하고, "
            "해당 분야의 전문 용어와 개념을 정확히 반영해주세요."
        )


# ────────────────────────────────────────
# 공통 STT 보정 지침 (모든 모드 공유)
# ────────────────────────────────────────

_STT_CORRECTION_GUIDE = """
## STT 보정 지침
- 음성 인식 오류로 인한 잘못된 단어를 문맥에 맞게 교정하세요
- 전문 용어, 고유명사, 영어 약어(예: API, CPU, GDP)는 올바른 표기로 교정하세요
- 발음이 비슷한 단어 혼동(예: "배열" vs "배려")을 문맥으로 판단하세요
- 교정한 주요 용어는 마지막 섹션에 기록하세요
""".strip()


